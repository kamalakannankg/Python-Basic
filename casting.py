#Casting
#when you want to specify a type on to a variable. 
# This can be done with casting

#Intergers
x=int(3)
y=int(85.2)
z=int("5")
print(x)
print(y)
print(z)


#Floats
x=float(5)
y=float(5.54)
z=float("3")
w=float("54.1")
print(x)
print(y)
print(z)
print(w)

#strings
x=str("s1")
y=str(1)
z=str(6.1)
print(x)
print(y)
print(z)